package collections_wapperClasses;

public class WapperClass {
	
	public static void main(String[] args) {
	
		int num = 10;
		// Autoboxing coverting primitive type to objects
	    Integer obj = num;
	    System.out.println(obj);
	    
	    int unbox = obj;
	    // unboxing coverting objects into primitive types
	    System.out.println(unbox);

}
}
